export type MaterialSeed = ScopedSeed & {
  code: string;
  name: string;
  category: MaterialCategory;
  status: RecordStatus;
  unit: string;
  quantity: number;
  unitCost: number;
  notes: string;
};

export type RuleSeed = ScopedSeed & {
  code: string;
  title: string;
  category: RuleCategory;
  outputType?: OutputType;
  status: RecordStatus;
  priority: number;
  description: string;
  ruleText: string;
  source: string;
};

export type QcTemplateSeed = ScopedSeed & {
  templateKey: string;
  name: string;
  category: QcCategory;
  status: RecordStatus;
  checklist: string[];
  acceptanceCriteria: string[];
  rejectionCriteria: string[];
};

export type GeneratedOutputSeed = {
  title: string;
  outputType: OutputType;
  status: OutputStatus;
  inputPayload: Record<string, unknown>;
  outputPayload: Record<string, unknown>;
};

